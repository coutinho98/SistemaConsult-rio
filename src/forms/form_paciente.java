        p.setEmail(jT_email.getText());
        p.setNascimento(jF_data.getText());
        p.setTelefone(jF_telefone.getText());
        p.setSexo(jC_sexo.getSelectedItem().toString());
        p.setTipo_medico(jC_tipoMedico.getSelectedItem().toString());
